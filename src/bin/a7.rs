use keccak_hash::keccak_256;

pub fn kecaak256(input: &[u8]) -> [u8; 32] {
    let mut data = [0u8; 32];
    keccak_256(input, &mut data);
    data
}

fn main() {
    let u8_data: [u8; 32] = [
        0x41, 0x42, 0x43, 0x44, 0x41, 0x42, 0x43, 0x44, 0x41, 0x42, 0x43, 0x44, 0x41, 0x42, 0x43,
        0x44, 0x41, 0x42, 0x43, 0x44, 0x41, 0x42, 0x43, 0x44, 0x41, 0x42, 0x43, 0x44, 0x41, 0x42,
        0x43, 0x44,
    ];
    let resp = kecaak256(&u8_data);
    println!("{:?}", resp);
}
